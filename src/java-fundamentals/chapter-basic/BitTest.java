/**
 * 测试位运算符
 */
public class BitTest {
    public static void main(String[] args) {
        int i = 3;
        System.out.println("3 << 2 = " + (i << 2));

        int m = 4;
        int n = 7;
        System.out.println("4 & 7 = " + (m & n));
        System.out.println("4 | 7 = " + (m | n));
        System.out.println("4 ^ 7 = " + (m ^ n));
    }
}

